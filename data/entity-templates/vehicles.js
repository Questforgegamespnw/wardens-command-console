function vehicleTemplate({
  id,
  label,
  description,
  group,
  groupLabel,
  sortOrder,
  platformType,
  role,
  scale,
  crew,
  wounds,
  healthPerWound,
  av,
  dr = 0,
  loadout = [],
  tags = [],
}) {
  return Object.freeze({
    id,
    label,
    type: "vehicle",

    category: "vehicle",
    categoryLabel: "Vehicles",
    group,
    groupLabel,
    sortOrder,

    description,

    defaults: Object.freeze({
      defense: Object.freeze({
        av,
        dr,
        coverAv: 0,
        armored: av > 0,
        tags: Object.freeze([
          "vehicle",
          ...(av > 0 ? ["armored"] : []),
        ]),
      }),

      health: Object.freeze({
        healthPerWound,
        currentHealth: healthPerWound,
        maximumWounds: wounds,
        woundsRemaining: wounds,
      }),

      conditions: Object.freeze({
        bleeding: 0,
        activeTac: Object.freeze([]),
        statuses: Object.freeze([]),
      }),

      armorSystems: Object.freeze({}),

      vehicle: Object.freeze({
        platformType,
        condition: "operational",

        systems: Object.freeze({
          mobility: Object.freeze([]),
          weapons: Object.freeze([]),
          sensors_fire_control: Object.freeze([]),
          crew_cockpit: Object.freeze([]),
          power_systems: Object.freeze([]),
          armor_structure: Object.freeze([]),
        }),

        heatClock: Object.freeze({
          current: 0,
          maximum: 4,
        }),

        activeCountdowns: Object.freeze([]),
      }),
    }),

    profile: Object.freeze({
      role,
      scale,
      crew,
      loadout: Object.freeze([...loadout]),
    }),

    tags: Object.freeze([
      "vehicle",
      "vehicle_wounds",
      ...tags,
    ]),

    status: "active",
  });
}

